# range function syntax - range(start argument , stop argument , step argument)

for i in range(1,11,2): # Here 1 is start argument which means that the loop will start form 1 and will end on 10 and will jump 2 numbers.
    print(i)

for i in range(10,0,-1):
    print(i)

for i in range(0,-11,-1):
    print(i)
